package com.elasticpath.rest.client.deserialization;

public class Self {

	private String href;
	private String uri;

	public String getHref() {
		return href;
	}

	public void setHref(String href) {
		this.href = href;
	}

	public String getUri() {
		return uri;
	}

	public void setUri(String uri) {
		this.uri = uri;
	}
}
